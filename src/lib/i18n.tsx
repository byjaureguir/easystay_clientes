// ─── Context ─────────────────────────────────────────────────────────────────

type Translations = typeof es;

type Ctx = {
  language: Lang;
  toggleLanguage: () => void;
  setLanguage: (l: Lang) => void;
  t: (key: keyof Translations) => string;
};

const LanguageContext = createContext<Ctx | null>(null);

export function useLanguage(): Ctx {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error("useLanguage must be used within LanguageProvider");
  return ctx;
}

function getInitialLang(): Lang {
  if (typeof window === "undefined") return "es";
  try {
    const stored = localStorage.getItem("easystay_lang");
    if (stored === "en" || stored === "es") return stored;
  } catch {}
  return "es";
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Lang>(getInitialLang);

  const setLanguage = useCallback((l: Lang) => {
    setLanguageState(l);
    try { localStorage.setItem("easystay_lang", l); } catch {}
    if (typeof document !== "undefined") document.documentElement.lang = l;
  }, []);

  const toggleLanguage = useCallback(() => {
    setLanguage(language === "es" ? "en" : "es");
  }, [language, setLanguage]);

  const t = useCallback(
    (key: keyof Translations): string => (language === "en" ? en[key] : es[key]),
    [language],
  );

  return (
    <LanguageContext.Provider value={{ language, toggleLanguage, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

